class E2_Sum
{
      public static void main(String args[])
      {
            int num1,num2,answer;
 
            num1=Integer.parseInt(args[0]);
            num2=Integer.parseInt(args[1]);
 
            answer=num1+num2;
            System.out.println(num1 + " + " + num2 +" = " +answer);
      }
}